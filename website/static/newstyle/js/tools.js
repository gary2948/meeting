function getlanguage(lan){
	switch(lan)
	{
	case "1":
	  return "中文"
	case "2":
	  return "英语"
	case "3":
	  return "日语"
	case "4":
	  return "朝鲜语"
	case "5":
	  return "法语"
	case "6":
	  return "西班牙语"
	case "7":
	  return "泰语"
	case "8":
	  return "阿拉伯语"
	case "9":
	  return "俄语"
	case "10":
	  return "葡萄牙语"
	default:
	  return "无"
	}
}